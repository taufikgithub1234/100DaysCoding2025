public class Main{
  public static void main(String[] args) {
    int a = 2;
    a += 12;
    System.out.println(a);
    a -= 12;
    System.out.println(a);
    a /= 12;
    System.out.println(a);
    a *= 12;
    System.out.println(a);
    a %= 12;
    System.out.println(a);
    a = 12;
    System.out.println(a);
  }
}
