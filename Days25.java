public class Main {
    public static void main(String[] args) {
        int angka = 5;

        // Increment (tambah 1)
        angka++;
        System.out.println("Setelah increment: " + angka); 

        // Decrement (kurang 1)
        angka--;
        System.out.println("Setelah decrement: " + angka);
    }
}
